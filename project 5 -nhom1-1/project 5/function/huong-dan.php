</center>
<div style="margin-top: -480px">
Trong màn hình danh sách các chuyến xe, bạn chú ý kiểm tra lại <b>Ngày đi</b> đã đúng yêu cầu của mình chưa (VD ở đây là chuyến xe <b>Hà Nội - Hồ Chí Minh</b> đi ngày <b>20/11/2017</b>). Bạn có thể chọn lại ngày đi bằng cách nhấn vào ô ngày đi để chọn lại. Để lọc xem các chuyến của một hãng xe nào đó, Bạn nhấn vào ô HÃNG XE để chọn. Để xem hình ảnh và thông tin chi tiết của từng xe, bạn nhấn vào nút Chi tiết của chuyến xe đó.<br><br>
<b style="text-decoration: underline;font-style: italic;">Bước 2:</b> Bạn chọn chuyến xe phù hợp với mình rồi bấm vào nút Đặt vé để tiến hành đặt vé. Bạn có thể đặt vé bằng cách gọi tổng đài để nhân viên hỗ trợ trực tiếp hoặc đặt vé trực tuyến bằng cách chọn Số vé và nhấn nút Đặt vé.
<br>
<b style="text-decoration: underline;font-style: italic;">Bước 2(b): </b> Sau khi đặt vé, trường hợp bạn chỉ muốn đặt 01 chiều, bạn sẽ chuyển luôn sang <b style="text-decoration: underline;font-style: italic;">Bước 3 </b>để thực hiện thanh toán. Trường hợp bạn muốn đặt luôn chiều về hoặc đặt thêm 01 chuyến xe khác bạn có thể nhấn nút Chọn ngày để đặt chiều về (VD ở đây là chọn ngày về Hồ Chí Minh - Hà Nội) hoặc nút Tìm chuyến nhanh để chọn một chuyến khác (VD: đặt thêm vé Sapa - Hạ Long,...).
<br><br>
<b style="text-decoration: underline;font-style: italic;">Bước 3:</b> Sau khi đã chọn xong số vé cần đặt, bạn ghi thông tin đặt vé của mình (tên, số điện thoại, email) và chọn phương thức thanh toán phù hợp (Có 07 hình thức thanh toán để bạn lựa chọn). Bạn nhập mã khuyến mãi (nếu có) để giảm giá rồi bấm Tiếp tục đặt vé.
<br><br>
<b style="text-decoration: underline;font-style: italic;">Bước 4:</b> Bạn xem lại toàn bộ thông tin đơn hàng của mình rồi bấm Kết thúc để xác nhận thanh toán. Trường hợp cần chỉnh sửa đơn hàng, bạn bấm nút Trở lại để điều chỉnh.

> Với các hình thức thanh toán sau (Chuyển khoản ngân hàng/ Ví điện tử Momo/ Thanh toán tại cửa hàng gần nhà/ Thanh toán trực tiếp tại Văn phòng/ Thanh toán tiền mặt tại nhà) hệ thống sẽ chuyển qua <b style="text-decoration: underline;font-style: italic;">Bước 5 </b>để kết thúc.

> Với các hình thức thanh toán trực tuyến (Thanh toán qua Internet Banking/ Thẻ tín dụng quốc tế) hệ thống sẽ chuyển bạn sang màn hình thanh toán của Ngân hàng/ Thẻ quốc tế để thực hiện thanh toán. Sau khi hoàn tất thanh toán, hệ thống sẽ chuyển lại qua <b style="text-decoration: underline;font-style: italic;">Bước 5 </b>để kết thúc.
<br><br>
<b style="text-decoration: underline;font-style: italic;">Bước 5:</b> Hệ thống sẽ gửi xác nhận đặt vé thành công và gửi vé điện tử đến email cho bạn. Trường hợp thanh toán trực tuyến (Thanh toán qua Internet banking/ Thẻ tín dụng quốc tế) trạng thái đơn hàng sẽ là Đã thanh toán. Các trường hợp thanh toán khác trạng thái đơn hàng sẽ là Chưa thanh toán, bạn chú ý thanh toán đơn hàng trong 72 giờ sau khi đặt vé và trước 24 giờ trước thời gian khởi hành.
<br><br><br>
 Sau khi bạn thực hiện thanh toán, hệ thống sẽ gửi email và tin nhắn sms xác nhận trạng thái đơn hàng Đã thanh toán cho bạn.



Đến ngày đi, bạn chỉ cần đọc tên và số điện thoại đã đặt vé của mình để lên xe.

 

